    private void btn_BuscarPuntosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarPuntosActionPerformed
        if (visualizarPuntos.getSelectedIndex()==0)
        {
            mostrarTablaPuntos("SELECT * FROM Puntos");
        }
        String verificador = txt_BuscarPunto.getText();
        if (!txt_BuscarPunto.getText().isBlank() && visualizarPuntos.getSelectedIndex() != 0) {
            switch (visualizarPuntos.getSelectedIndex()) {
                case 1:
                    String validos = "0123456789";
                    int contando_Correctos = 0;
                    for (int i = 0; i < verificador.length(); i++) {
                        for (int u = 0; u < validos.length(); u++) {
                            if (verificador.charAt(i) == validos.charAt(u)) {
                                contando_Correctos++;
                            }
                        }
                    }   if (contando_Correctos == verificador.length()) {
                        String sql2 = "SELECT * FROM Puntos WHERE id_ruta = " + Integer.parseInt(verificador);
                        mostrarTablaPuntos(sql2);
                    } else {
                        JOptionPane.showMessageDialog(null, "NO ES UN NUMERO");
                    }   break;
                case 2:
                    {
                        String sql2 = "SELECT * FROM Puntos WHERE encargado ='" + verificador+"'";
                        mostrarTablaPuntos(sql2);
                        break;
                    }
                case 3:
                    {
                        String sql2 = "SELECT * FROM Puntos WHERE id_punto ='" + verificador + "'";
                        mostrarTablaPuntos(sql2);
                        break;
                    }
                case 4:
                    {
                        String sql2 = "SELECT codigo_ruta FROM Ruta WHERE destino ='" + verificador + "'";
                        Statement st;
                        int numeroRuta=0;
                        try {
                            st = cn1.cn.createStatement();
                            ResultSet rs = st.executeQuery(sql2);
                            while (rs.next()) {
                                numeroRuta = rs.getInt("codigo_ruta");
                            }
                        } catch (SQLException e) {
                            
                        }       mostrarTablaPuntos("SELECT * FROM Puntos WHERE id_ruta ="+numeroRuta);
                        break;
                    }
                default:
                    break;
            }
        }
        
        
    }//GEN-LAST:event_btn_BuscarPuntosActionPerformed

