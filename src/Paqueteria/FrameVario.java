    private void btn_GenerarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GenerarFacturaActionPerformed
        if (!entNit.getText().isBlank()) {
            for (int i = 0; i < modelo.getRowCount(); i++) {
                String codigo = modelo.getValueAt(i, 0).toString();
                String destina = modelo.getValueAt(i, 2).toString();
                String remitente = modelo.getValueAt(i, 3).toString();
                int peso = Integer.parseInt(modelo.getValueAt(i, 4).toString());
                String costo = modelo.getValueAt(i, 5).toString();
                String priori = modelo.getValueAt(i, 6).toString();
                boolean priorizacion = false;
                if (!priori.equalsIgnoreCase("no")) {
                    priorizacion = true;
                }
                Calendar c1 = new GregorianCalendar();
                int dia = c1.get(Calendar.DATE);
                int mes = c1.get(Calendar.MONTH);
                int anio = c1.get(Calendar.YEAR);
                String fecha = "" + anio + "-" + mes + "-" + dia;
                int ruta1 = nuevoPa.nuevo.ruta(modelo.getValueAt(i, 1).toString());
                int tarifaG = nuevoPa.tarifaGlob;
                nuevoPa.nuevo.agregarDatosPaquete(codigo, ruta1, "BODEGA", destina, remitente, "", tarifaG, priorizacion, entNit.getText(), peso);
                nuevoPa.nuevo.agregarAGanancias(codigo, ruta1, Integer.parseInt(costo), fecha);
                nuevoPa.nuevo.agregarABodega1(codigo, ruta1, priorizacion);
                if (entNombreCliente.isEditable() && entCiudadCliente.isEditable() && entNit.getText().length()==8){
                    nuevoPa.nuevo.agregarCliente(entNit.getText(), entNombreCliente.getText(),entCiudadCliente.getText());
                } 
                JOptionPane.showMessageDialog(null, "PAQUETE INGRESADO EXITOSAMENTE");
                nuevoPa.limpiarTodo();
                this.dispose();
            }

        }

    }//GEN-LAST:event_btn_GenerarFacturaActionPerformed

    private void btn_BuscarNitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_BuscarNitActionPerformed
        if (!entNit.getText().isBlank()){
            if (!entNit.getText().equalsIgnoreCase("cf") && !entNit.getText().equalsIgnoreCase("c/f")){
                if (entNit.getText().length()==8){
                    clientes = nuevoPa.nuevo.clienteNit(entNit.getText());
                    if (!clientes[0].equalsIgnoreCase("nuevo")){
                        entNombreCliente.setText(clientes[0]);
                        entCiudadCliente.setText(clientes[1]);
                        entNombreCliente.setEditable(false);
                        entCiudadCliente.setEditable(false);
                    }
                }
            } else {
                entNombreCliente.setText("C/F");
                entCiudadCliente.setText("C/F");
                entNombreCliente.setEditable(false);
                entCiudadCliente.setEditable(false);
            }
        }
        btn_GenerarFactura.setEnabled(true);
    }//GEN-LAST:event_btn_BuscarNitActionPerformed
    public void mostrarTabla(){
        modelo2 = new DefaultTableModel();
        modelo2.addColumn("DESCRIPCION");
        modelo2.addColumn("CANTIDAD");
        modelo2.addColumn("SUBTOTAL");
        tablaFactura.setModel(modelo2);
        String descripcion = "";
        String datos[] = new String[3];
        int priorizado = 0;
        int costoTotal = 0;
        for (int i = 0; i < modelo.getRowCount(); i++){
            String codigo = modelo.getValueAt(i, 0).toString();
            String ruta = modelo.getValueAt(i, 1).toString();
            String destina = modelo.getValueAt(i, 2).toString();
            String costo = modelo.getValueAt(i, 5).toString();
            String priori = modelo.getValueAt(i, 6).toString();
            costoTotal += Integer.parseInt(modelo.getValueAt(i, 5).toString());
            if(!priori.equalsIgnoreCase("no")){
                priorizado++;
            }
            datos[0] = "Paquete "+codigo+" en la ruta "+ruta+" a entregarselo a "+destina;
            datos[1] = "1";
            datos[2] = "Q"+costo+".00";
            modelo2.addRow(datos);
        }
        int tarifaPriori = nuevoPa.costos1[1];
        int totalPriori = priorizado*tarifaPriori;
        datos[0] = "Cuota EXTRA por PRIORIZACION ya incluida";
        datos[1] = ""+priorizado;
        datos[2] = "Q"+totalPriori+".00 (Ya incluido)";
        modelo2.addRow(datos);
        datos[0] = "Total";
        datos[1] = " ";
        datos[2] = "Q"+costoTotal+".00";
        modelo2.addRow(datos);
        tablaFactura.setModel(modelo2);
    }

